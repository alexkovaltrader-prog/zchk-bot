user_states = {}
user_locks  = {}

def get_state(uid): return user_states.get(uid, {})
def set_state(uid, s): user_states[uid] = s
def get_lock(uid):
    if uid not in user_locks:
        user_locks[uid] = asyncio.Lock()
    return user_locks[uid]

