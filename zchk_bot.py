# ── META CONVERSIONS API ─────────────────────────────────────────────────────

def sha256(value: str) -> str:
    return hashlib.sha256(value.strip().lower().encode("utf-8")).hexdigest()


def get_source_from_context(context: ContextTypes.DEFAULT_TYPE) -> str:
    try:
        if context.args and len(context.args) > 0:
            return context.args[0]
    except Exception:
        pass
    return "direct"


def parse_payload(source: str) -> dict:
    """
    Разбираем payload из /start:
    landing1__utm_source__utm_campaign__utm_adset__utm_ad__fbp__fbc
    """
    parts = source.split("__")
    return {
        "landing":      parts[0] if len(parts) > 0 else "unknown",
        "utm_source":   parts[1] if len(parts) > 1 else "direct",
        "utm_campaign": parts[2] if len(parts) > 2 else "none",
        "utm_adset":    parts[3] if len(parts) > 3 else "none",
        "utm_ad":       parts[4] if len(parts) > 4 else "none",
        "fbp":          parts[5] if len(parts) > 5 and parts[5] != "none" else None,
        "fbc":          parts[6] if len(parts) > 6 and parts[6] != "none" else None,
    }


def build_user_data(user, payload: dict | None = None) -> dict:
    """
    Идентификаторы пользователя для Meta CAPI.
    fbp и fbc передаются с лендинга через /start payload — сшивают
    браузерного пользователя с серверными событиями из бота.
    """
    user_data = {
        "external_id": sha256(str(user.id)),
    }
    if payload:
        if payload.get("fbp"):
            user_data["fbp"] = payload["fbp"]
        if payload.get("fbc"):
            user_data["fbc"] = payload["fbc"]
    return user_data


async def send_meta_event(
    event_name: str,
    user,
    source: str = "direct",
    custom_data: dict | None = None,
    payload: dict | None = None,
):
    if not META_ACCESS_TOKEN:
        logging.warning(f"Meta CAPI skipped: META_ACCESS_TOKEN is empty. Event={event_name}")
        return None

    parsed = payload or parse_payload(source)

    event = {
        "event_name": event_name,
        "event_time": int(time.time()),
        "action_source": "chat",
        "event_id": f"{event_name}_{user.id}_{int(time.time())}_{uuid.uuid4().hex[:8]}",
        "user_data": build_user_data(user, parsed),
        "custom_data": {
            "source":           source,
            "landing":          parsed.get("landing", "unknown"),
            "utm_source":       parsed.get("utm_source", "direct"),
            "utm_campaign":     parsed.get("utm_campaign", "none"),
            "utm_adset":        parsed.get("utm_adset", "none"),
            "utm_ad":           parsed.get("utm_ad", "none"),
            "telegram_id":      str(user.id),
            "telegram_username": user.username or "",
            "telegram_name":    user.full_name or "",
            **(custom_data or {}),
        },
    }

    meta_payload = {"data": [event]}
    if META_TEST_EVENT_CODE:
        meta_payload["test_event_code"] = META_TEST_EVENT_CODE

    url = f"https://graph.facebook.com/{META_API_VERSION}/{META_PIXEL_ID}/events"
    params = {"access_token": META_ACCESS_TOKEN}

    try:
        async with httpx.AsyncClient(timeout=15) as client:
            response = await client.post(url, params=params, json=meta_payload)
            if response.status_code >= 400:
                logging.error(f"Meta CAPI error {response.status_code}: {response.text}")
            else:
                logging.info(f"Meta CAPI sent: {event_name} | utm_source={parsed.get('utm_source')} | fbp={'yes' if parsed.get('fbp') else 'no'}")
            return response.json()
    except Exception as e:
        logging.error(f"Meta CAPI request failed: {e}")
        return None


