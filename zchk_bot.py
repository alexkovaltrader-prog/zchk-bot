async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user   = update.effective_user
    source = get_source_from_context(context)
    parsed = parse_payload(source)

    set_state(user.id, {
        "source":         source,
        "payload":        parsed,
        "path":           [],
        "quiz_started":   False,
        "quiz_completed": False,
    })

    await send_meta_event(
        "BotStarted",
        user,
        source,
        custom_data={"bot": "zchkacademy_bot"},
        payload=parsed,
    )

    q  = QUESTIONS["start"]
    kb = [[InlineKeyboardButton(opt, callback_data=f"q:start:{i}")] for i, (opt, _) in enumerate(q["opts"])]
    welcome = (
        f"Привет, {user.first_name}\n\n"
        f"Я Ярослав Зайцев,основатель ZCHK Academy и ZCHK Capital Fund, платформы по трейдингу.\n\n"
        f"Выше ты видишь малую часть моих результатов,и меня, который сейчас задаст тебе 5 вопросов, "
        f"чтобы я смог точнее подсказать дальнейшие шаги и ты смог делать такой же результат.\n\n"
        f"*{q['text']}*"
    )
    photo = await fetch_photo(f"{GITHUB_BASE}/Frame%20307.png")
    if photo:
        await update.message.reply_photo(photo=photo, caption=welcome, parse_mode="Markdown", reply_markup=InlineKeyboardMarkup(kb))
    else:
        await update.message.reply_text(welcome, parse_mode="Markdown", reply_markup=InlineKeyboardMarkup(kb))


async def button(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    user  = update.effective_user
    data  = query.data

    if data.startswith("q:"):
        lock = get_lock(user.id)
        if lock.locked(): return
        async with lock:
            await handle_question(query, context, user, data)
    elif data == "show_story":
        await handle_story(query, context)
    elif data == "show_why":
        await handle_why(query, context)
    elif data == "cta:call":
        await handle_cta_call(query, context, user)
    elif data == "cta:platform":
        await handle_cta_platform(query, context, user)
    elif data == "restart":
        set_state(user.id, {})
        await start_from_callback(query, context, user)


async def handle_question(query, context, user, data):
    _, q_key, idx_str = data.split(":")
    idx = int(idx_str)
    q   = QUESTIONS[q_key]
    selected_text, next_key = q["opts"][idx]

    state   = get_state(user.id)
    source  = state.get("source", "direct")
    payload = state.get("payload")

    if not state.get("quiz_started"):
        state["quiz_started"] = True
        await send_meta_event(
            "QuizStarted",
            user,
            source,
            custom_data={"first_question": q_key, "first_answer": selected_text},
            payload=payload,
        )

    state.setdefault("path", []).append({
        "question_key": q_key,
        "question":     q.get("text", ""),
        "answer":       selected_text,
        "next_key":     next_key,
    })
    set_state(user.id, state)

    if next_key.startswith("result_"):
        await send_result(query, context, user, next_key)
    else:
        next_q = QUESTIONS[next_key]
        kb = [[InlineKeyboardButton(opt, callback_data=f"q:{next_key}:{i}")] for i, (opt, _) in enumerate(next_q["opts"])]
        try:
            await query.edit_message_text(f"*{next_q['text']}*", parse_mode="Markdown", reply_markup=InlineKeyboardMarkup(kb))
        except Exception:
            await context.bot.send_message(chat_id=query.message.chat_id, text=f"*{next_q['text']}*", parse_mode="Markdown", reply_markup=InlineKeyboardMarkup(kb))


async def send_result(query, context, user, result_key):
    r       = RESULTS[result_key]
    track   = r["track"]
    chat_id = query.message.chat_id

    state       = get_state(user.id)
    source      = state.get("source", "direct")
    payload     = state.get("payload")
    path        = state.get("path", [])
    temperature = "hot" if result_key.endswith("_hot") else "cold"

    state["quiz_completed"] = True
    state["result_key"]     = result_key
    state["track"]          = track
    state["temperature"]    = temperature
    set_state(user.id, state)

    await send_meta_event(
        "QuizCompleted",
        user,
        source,
        custom_data={
            "result_key":    result_key,
            "track":         track,
            "temperature":   temperature,
            "answers_count": len(path),
            "path":          " > ".join([p.get("answer", "") for p in path])[:900],
        },
        payload=payload,
    )

    # Lead из бота — промежуточный (дошёл до конца квиза)
    # Финальный Lead фиксируется пикселем на dashboard.html
    await send_meta_event(
        "QuizLead",
        user,
        source,
        custom_data={
            "lead_type":   "quiz_completed",
            "result_key":  result_key,
            "track":       track,
            "temperature": temperature,
        },
        payload=payload,
    )

    try:
        await context.bot.send_message(
            LEADS_CHAT_ID,
            f"🔥 *Новый лид*\n\n👤 {user.full_name} (@{user.username or 'нет'})\n🎯 Сегмент: `{result_key}`\n📡 UTM: `{source}`",
            parse_mode="Markdown"
        )
    except Exception as e:
        logging.error(f"Lead notify failed: {e}")

    try:
        warmup_msgs = WARMUP.get(result_key, WARMUP.get(f"result_{track}_11_hot", []))
        for i, msg in enumerate(warmup_msgs):
            context.job_queue.run_once(
                send_warmup,
                (i + 1) * 86400,
                data={"chat_id": chat_id, "text": msg, "step": i},
                name=f"warmup_{chat_id}_{i}"
            )
    except Exception as e:
        logging.error(f"Warmup schedule failed: {e}")

    kb = [
        [InlineKeyboardButton("История Ярослава",                   callback_data="show_story")],
        [InlineKeyboardButton("Записаться на звонок с Ярославом",   callback_data="cta:call")],
        [InlineKeyboardButton("Разобраться на платформе бесплатно", callback_data="cta:platform")],
    ]

    photo = await fetch_photo(f"{GITHUB_BASE}/{RESULT_PHOTOS[track]}")
    if photo:
        try:
            await context.bot.send_photo(chat_id=chat_id, photo=photo, caption=r["text"], parse_mode="Markdown", reply_markup=InlineKeyboardMarkup(kb))
            return
        except Exception as e:
            logging.error(f"Result photo failed: {e}")

    await context.bot.send_message(chat_id=chat_id, text=r["text"], parse_mode="Markdown", reply_markup=InlineKeyboardMarkup(kb))


